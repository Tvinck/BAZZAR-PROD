/**
 * Get expert by ID
 */
export const getExpertById = (id) => EXPERTS.find(e => e.id === id);

/**
 * Get expert's full system prompt with context
 */
export const getExpertPrompt = (expertId, userName = 'пользователь') => {
    const expert = getExpertById(expertId);
    if (!expert) return null;

    return `${expert.systemPrompt}

Имя пользователя: ${userName}
Текущая дата: ${new Date().toLocaleDateString('ru-RU')}

Отвечай на русском языке.`;
};
